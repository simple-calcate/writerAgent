export const PLAN_TRIGGERS = ['规划', '计划', '大纲', '接下来怎么写', '剧情走向', '后续发展', '/plan']

export function detectPlanMode(userMessage: string): boolean {
  return PLAN_TRIGGERS.some(keyword => userMessage.includes(keyword))
}
